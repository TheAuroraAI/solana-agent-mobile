function applyFilters(
  tokens: ScreenerToken[],
  category: string,
  minMcap: number,
  maxMcap: number,
  sort: SortField,
  dir: SortDir,
): ScreenerToken[] {
  let result = [...tokens];

  if (category && category !== 'all') {
    result = result.filter((t) => t.category === category);
  }

  if (minMcap > 0) {
    result = result.filter((t) => t.marketCap >= minMcap);
  }
  if (maxMcap > 0) {
    result = result.filter((t) => t.marketCap <= maxMcap);
  }

  const fieldMap: Record<SortField, keyof ScreenerToken> = {
    marketCap: 'marketCap',
    volume24h: 'volume24h',
    price: 'price',
    change24h: 'change24h',
    change7d: 'change7d',
    txCount: 'txCount24h',
    holders: 'holders',
  };

  const key = fieldMap[sort];
  result.sort((a, b) => {
    const av = a[key] as number;
    const bv = b[key] as number;
    return dir === 'asc' ? av - bv : bv - av;
  });

  return result;
}

export async function GET(request: NextRequest) {
  const { searchParams } = new URL(request.url);
  const sort = (searchParams.get('sort') ?? 'marketCap') as SortField;
  const dir = (searchParams.get('dir') ?? 'desc') as SortDir;
  const category = searchParams.get('category') ?? 'all';
  const minMcap = Number(searchParams.get('minMcap') ?? '0');
  const maxMcap = Number(searchParams.get('maxMcap') ?? '0');

  const tokens = applyFilters(ALL_TOKENS, category, minMcap, maxMcap, sort, dir);

  const data: ScreenerData = {
    tokens,
    totalCount: tokens.length,
    lastUpdated: new Date().toISOString(),
  };

  return NextResponse.json(data);
}
